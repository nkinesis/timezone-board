var UTILS = {
    detectLocale: function () {
        // if the user understands portuguese, prefer portuguese
        var langs = window.navigator.languages ? window.navigator.languages : [];
        if (langs.indexOf("pt-BR") > -1) {
            return "pt-BR";
        }
        return window.navigator.language;
    },
    isWorkingHours: function (hour) {
        // 9-to-5 shift +1h (since Brazil is usually 8-to-6)
        return (hour > 8 && hour < 18);
    },
    isWeekend: function (weekDay) {
        // saturday or sunday
        return (weekDay == 0 || weekDay == 6);
    },
    isValidTimezone: function (timezoneName) {
        return TIMEZONES.indexOf(timezoneName);
    },
}

var APP = {
    locale: UTILS.detectLocale(),
    lastUpdateElement: document.querySelector("#lastUpdate"),
    board: null,
    people: [],
    state: 0, //initial loading

    init: function () {
        APP.updateBoard();
        setInterval(function () {
            if (APP.state == 1) {
                APP.updateBoard();
            }
        }, 30000);
    },

    setLastUpdate() {
        let dateObj = new Date();
        let formattedDate = dateObj.toLocaleString(APP.locale);
        APP.lastUpdateElement.innerText = formattedDate;
    },

    updateBoard: function () {
        var jsonUrl = window.localStorage.getItem('jsonURL');
        if (!jsonUrl) {
            jsonUrl = prompt("Please inform your JSON file URL:");
            if (jsonUrl) {
                window.localStorage.setItem('jsonURL', jsonUrl);
            } else {
                APP.state = 2; //error
                alert("Invalid URL! Please reload the page and try again.");
            }
        }

        let requestOptions = {
            method: 'GET',
            redirect: 'follow'
        };
        
        fetch(jsonUrl, requestOptions)
            .then(response => response.text())
            .then(function (result) {
                APP.people = JSON.parse(result);
                APP.createBoard();
                APP.setLastUpdate();
                APP.state = 1; //ok
            })
            .catch(function (error) {
                APP.state = 2; //error
                console.log(error);
                alert("Error: " + error);
            });
    },

    getPersonTime: function (timezoneName) {
        //check if timezone exists
        let tzExists = UTILS.isValidTimezone(timezoneName);
        if (!tzExists) {
            return { "fullDate": "Invalid timezone", "workingHours": false, "weekend": true };
        }
        let dateObj = new Date();
        let isoStr = dateObj.toISOString();
        dateObj = new Date(isoStr);
        let fullDate = dateObj.toLocaleString(APP.locale, { timeZone: timezoneName });
        let workingHours = UTILS.isWorkingHours(dateObj.getHours());
        let weekend = UTILS.isWeekend(dateObj.getDay());
        return { "fullDate": fullDate, "workingHours": workingHours, "weekend": weekend };
    },

    createBoard: function () {
        let counter = 1;
        APP.board = document.createElement("tbody");

        for (let person of APP.people) {
            let rowClassName = "";

            // table row and columns
            let row = document.createElement("tr");
            let td1 = document.createElement("td");
            let td2 = document.createElement("td");
            let td3 = document.createElement("td");
            let td4 = document.createElement("td");
            let td5 = document.createElement("td");

            let personTime = APP.getPersonTime(person.timezone);

            td1.innerText = person.name;
            td2.innerText = person.company;
            td3.innerText = person.country;

            // show current time and timezone
            td4.innerText = personTime.fullDate;
            td4.setAttribute("title", person.timezone);

            // show if person is in working hours
            td5.innerText = (personTime.workingHours && !personTime.weekend) ? "yes" : "no";
            if (!personTime.workingHours || personTime.weekend) {
                rowClassName = " off";
            } else {
                rowClassName = "";
            }

            // assemble table
            row.appendChild(td1);
            row.appendChild(td2);
            row.appendChild(td3);
            row.appendChild(td4);
            row.appendChild(td5);

            row.className = "table-light" + rowClassName;
            if (counter % 2 == 0) {
                row.className = "table-primary" + rowClassName;
            }

            APP.board.appendChild(row);
            counter++;
        }
        let curTbody = document.querySelector("tbody");
        let curTable = curTbody.parentNode;
        curTable.removeChild(curTbody);
        curTable.appendChild(APP.board);
    },

}

APP.init();